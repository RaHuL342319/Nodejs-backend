/**
 *      Comparison Operator
 *      >     greater than
 *      >=    greater than equal to
 *      <      less than
 *      <=      less than equal to
 *      ==      abstract equality
 *      ===     strict equality
 *      !=      abstract not equlas to
 *      !==     strict not equal to
 */

console.log(1>3);   // false
console.log(1>=1);  // true
console.log(3<4);   // true
console.log(3<=4);  // true
console.log(5===5); // true
console.log(5!==5); // false
