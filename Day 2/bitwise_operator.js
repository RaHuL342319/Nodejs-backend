/**
 *         Bitwise Operator 
 *                          These Operators doenot directly operate on numbers, 
 *                           but instead operate on binary representation of number.
 * 
 *          &    Bitwise AND
 *          |    Bitwise OR
 *          ^    Bitwise XOR
 *          ~    Bitwise not
 *          >>   Right shift
 *          <<   Left shift
 */


console.log(5&3); // 101 & 011 => 001 i.e. 1

console.log(5|2); // 101 & 010 => 111 i.e. 7

console.log(5^3); // 101 & 011 => 110 i.e. 6

console.log(~2);  // toggle the bit, you can learn from google  // -3

console.log(4<<1); // 100 left shift by 1 i.e. 1000 => 8

console.log(4>>1); // 100 right shift by 1 i.e. 10 => 2