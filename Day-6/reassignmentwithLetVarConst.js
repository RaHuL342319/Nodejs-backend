// var :  re-assignment is allowed
var x = 12;
console.log(x);

x = 23;   // re-assigning
console.log(x);


// let :  re-assignment is allowed
var y = 14;
console.log(y);

y = 23;   // re-assigning
console.log(y);


// const :  re-assignment is not allowed
const z = 14;
console.log(z);

z = 23;   // re-assigning
console.log(z);   // it will give you an error Assignment to constant variable.